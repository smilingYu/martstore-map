const apiKey = 'AIzaSyDr96M7MxqU-5LoZYxATCh_YY-_y0B4tNc';
const geocodeUrl = 'https://maps.googleapis.com/maps/api/geocode/json';

// 清理地址格式
function cleanAddress(address) {
    return address.trim().replace(/\s+/g, ' ').replace(/[()（）]/g, '');
}

// 延遲函數
function delay(ms) {
    return new Promise(resolve => setTimeout(resolve, ms));
}

// 地址轉經緯度
async function geocodeAddress(address) {
    const cleanedAddress = cleanAddress(address);
    const url = `${geocodeUrl}?address=${encodeURIComponent(cleanedAddress)}&key=${apiKey}`;
    
    try {
        const response = await fetch(url);
        const data = await response.json();
        if (data.status === 'OK' && data.results[0]) {
            const { lat, lng } = data.results[0].geometry.location;
            return { lat, lng, status: 'success' };
        } else {
            console.error(`Geocoding failed for address: ${cleanedAddress}, status: ${data.status}`);
            return { lat: null, lng: null, status: 'failed', error: data.status };
        }
    } catch (error) {
        console.error(`Error geocoding address: ${cleanedAddress}`, error);
        return { lat: null, lng: null, status: 'error', error: error.message };
    }
}

